#include <mpblas_dd.h>
#include <mplapack_dd.h>

void Rlassq(mplapackint const n, dd_real *x, mplapackint const incx, dd_real &scale, dd_real &sumsq) {
    //
    //  -- LAPACK auxiliary routine --
    //  -- LAPACK is a software package provided by Univ. of Tennessee,    --
    //  -- Univ. of California Berkeley, Univ. of Colorado Denver and NAG Ltd..--
    //
    //     .. Scalar Arguments ..
    //     ..
    //     .. Array Arguments ..
    //     ..
    //
    // =====================================================================
    //
    //     .. Parameters ..
    //     ..
    //     .. Local Scalars ..
    //     ..
    //     .. External Functions ..
    //     ..
    //     .. Intrinsic Functions ..
    //     ..
    //     .. Executable Statements ..
    //
    mplapackint ix = 0;
    dd_real absxi = 0.0;
    const dd_real zero = 0.0;
    if (n > 0) {
        for (ix = 1; ix <= 1 + (n - 1) * incx; ix = ix + incx) {
            absxi = abs(x[ix - 1]);
            if (absxi > zero || Risnan(absxi)) {
                if (scale < absxi) {
                    sumsq = 1 + sumsq * pow2((scale / absxi));
                    scale = absxi;
                } else {
                    sumsq += pow2((absxi / scale));
                }
            }
        }
    }
    //
    //     End of Rlassq
    //
}
